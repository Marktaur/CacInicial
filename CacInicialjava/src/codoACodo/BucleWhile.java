package codoACodo;



public class BucleWhile {

	public static void main(String[] args) {
		// variables
		int contador;
		contador = 1;
	
		
		while (contador<1000) {
			System.out.println("soy un contador y la cuenta es");
			System.out.println(contador);
			
			
			contador ++;
			
		}
		
	    System.out.println("Programa Finalizado");

	}
}

    
    
    
    